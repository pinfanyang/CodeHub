%小椭圆储油罐无变位:根据v-h公式求解罐容表
clear;clc;
h=0:0.01:1.2;
v=5/9*(-13083/10000*(18/25-3/5*h).^(1/2).*h+4361/2000*(18/25-3/5*h).^(1/2).*h.^2+39249/125000*15^(1/2)*asin(1/6*30^(1/2)*h.^(1/2)).*h.^(1/2))./h.^(1/2)*15^(1/2) %标定值
plot(h,v)
grid on
axis([0 1.2 0 5.0])
xlabel('油位高度h/m')
ylabel('储油量V/L')
title('小椭圆储油罐无变位时V-h关系图')